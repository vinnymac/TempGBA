#ifndef __DS2_TYPES_H__
#define __DS2_TYPES_H__

#ifndef u8
typedef unsigned char u8;
#endif

#ifndef s8
typedef char s8;
#endif

#ifndef u16
typedef unsigned short u16;
#endif

#ifndef s16
typedef short s16;
#endif

#ifndef u32
typedef unsigned int u32;
#endif

#ifndef s32
typedef int s32;
#endif

#ifndef u64
typedef unsigned long long u64;
#endif

#ifndef s64
typedef long long s64;
#endif 

#ifndef NULL
#define NULL 0
#endif

#ifndef TRUE
#define TRUE 1
#define FALSE 0
#endif

#endif //__DS2_TYPES_H__
